    public static char getAltChar(int keyCode) {
        char result = '\0';

        int lo = keyCode & 0x00FF;
        int hi = (keyCode & 0xFF00) >> 8;

        if (hi == 0x04) {
            if (lo >= 0x41 && lo <= 0x5A) {
                result = (char) (lo);
            } else if (lo >= 0x30 && lo <= 0x39) {
                result = (char) (lo);
            } else if (lo == 0x20) {
                result = ' ';
            } else if (lo == 0x2D) {
                result = '-';
            } else if (lo == 0x3D) {
                result = '=';
            }
        }

        return result;
    }
